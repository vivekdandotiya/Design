const seedDatabase = async (): Promise<void> => {
  try {
    await connectDB();
    console.log('\n🌱 Starting database seed...\n');

    // Clear existing products
    const deletedCount = await Product.deleteMany({});
    console.log(`🗑️  Cleared ${deletedCount.deletedCount} existing products`);

    // Insert all laptops
    const inserted = await Product.insertMany(laptops);
    console.log(`✅ Successfully inserted ${inserted.length} laptops:\n`);

    inserted.forEach((product, index) => {
      console.log(
        `   ${index + 1}. ${product.name} - ${product.brand} - ₹${product.price.toLocaleString('en-IN')} (Benchmark: ${product.benchmarkScore})`
      );
    });

    console.log('\n🎉 Database seeded successfully!\n');
    process.exit(0);
  } catch (error) {
    const err = error as Error;
    console.error(`\n❌ Seed error: ${err.message}\n`);
    process.exit(1);
  }
};

seedDatabase();
